//Grade Calculator: Use if-else if to assign grades (A, B, C, F) based on a score.
let score = 35;


if (score >= 90) {
  console.log('Score: ' + score + ', Grade: A' );
} else if (score >= 80) {
  console.log('Score: ' + score + ', Grade: B' );
} else if (score >= 70) {
  console.log('Score: ' + score + ', Grade: C' );
} else {
  console.log('Score: ' + score + ', Grade: F' );
}       
